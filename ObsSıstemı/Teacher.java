package ObsSýstemý;

public class Teacher {
 private String  name;
 private String  branch;

public Teacher() {
	
}
public Teacher(String name, String branch) {
	
	this.name = name;
	this.branch = branch;
}
public String getName() {
	return name;
}
public String getBranch() {
	return branch;
}
public void setName(String name) {
	this.name = name;
}
public void setBranch(String branch) {
	this.branch = branch;
}
 
 
 
 
 
 
 
 
 
 
}
